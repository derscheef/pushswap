#include "libft.h"

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	char	*new_src;

	new_src = malloc(ft_strlen(src) * sizeof(char) + 1);
	if (!new_src)
		return (0);
	ft_strcpy(new_src, src);
	return (new_src);
}
