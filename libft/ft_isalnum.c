#include "libft.h"

int	ft_isalnum(int input)
{
	if ((input >= 'a' && input <= 'z') || (input >= 'A' && input <= 'Z'))
		return (1);
	else if (input >= '0' && input <= '9')
		return (1);
	else
		return (0);
}
